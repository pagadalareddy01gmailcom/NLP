def question_three():
    """
    Extract all dates in DD Month YYYY format.

    Returns:
        list: List of matched date strings
    """
    # Regex to match day, month name, and year
    # DD: 01-31
    # Month: Abbreviated (Mar) or Full (March), with optional period
    # YYYY: Four digits
    day_pattern = r'(?:0?[1-9]|[12][0-9]|3[01])'
    month_names = r'(?:Jan(?:uary)?|Feb(?:ruary)?|Mar(?:ch)?|Apr(?:il)?|May|Jun(?:e)?|Jul(?:y)?|Aug(?:ust)?|Sep(?:tember)?|Oct(?:ober)?|Nov(?:ember)?|Dec(?:ember)?)'
    year_pattern = r'\d{4}'

    # Combine patterns with flexible separators and optional period for month
    pattern = fr'\b{day_pattern}\s+{month_names}\.?\s+{year_pattern}\b'

    results = []
    for note in df:
        matches = re.findall(pattern, note)
        results.extend(matches)

    return results

