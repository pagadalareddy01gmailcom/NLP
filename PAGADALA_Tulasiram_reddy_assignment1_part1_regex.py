def question_two():
    """
    Extract all dates with month names (e.g., Mar 20, 2009).

    Returns:
        list: List of matched date strings
    """
    # Regex to match month name, day, and year combinations
    # Handles abbreviated (Jan) and full (January) month names
    # Accounts for optional period after abbreviation (Mar.)
    # Allows for various separators: space, hyphen, comma
    month_names = r'(?:Jan(?:uary)?|Feb(?:ruary)?|Mar(?:ch)?|Apr(?:il)?|May|Jun(?:e)?|Jul(?:y)?|Aug(?:ust)?|Sep(?:tember)?|Oct(?:ober)?|Nov(?:ember)?|Dec(?:ember)?)'
    day_pattern = r'(?:0?[1-9]|[12][0-9]|3[01])'
    year_pattern = r'\d{4}'
    separator = r'(?:\s+|\-|\,\s*)'

    pattern = fr'\b{month_names}\.?(?:{separator}{day_pattern})?(?:{separator}{year_pattern})?\b'

    # Adjusting for specific examples given in Q2:
    # Mar-20-2009; Mar 20, 2009; March 20, 2009; Mar. 20, 2009; Mar 20 2009
    # These all include a day, so `day_pattern` and `year_pattern` should not be optional.
    # Let's ensure the pattern is robust for these exact cases.
    pattern = fr'\b{month_names}\.?(?:\s+|\-|\,\s*){day_pattern}(?:\s+|\-|\,\s*){year_pattern}\b'

    results = []
    for note in df:
        # re.findall finds all non-overlapping matches
        matches = re.findall(pattern, note)
        results.extend(matches)

    return results

