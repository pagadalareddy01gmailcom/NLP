    # Regex to capture phone numbers in various formats:
    # (XXX) XXX-XXXX, XXX-XXX-XXXX, XXX.XXX.XXXX, XXX XXX XXXX
    # It captures the three digit groups separately.
    pattern = r'\(?(\d{3})\)?[ .-]?(\d{3})[ .-]?(\d{4})'

