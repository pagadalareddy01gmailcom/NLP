def question_one(df):
    """
    Extract all dates in MM/DD/YY or MM/DD/YYYY format.

    Returns:
        list: List of matched date strings
    """
    # YOUR CODE HERE
    # Pattern for MM/DD/YY or MM/DD/YYYY
    # Month: 01-12 (single digit months allowed, e.g., 4/)
    # Day: 01-31 (single digit days allowed, e.g., /3/)
    # Year: 2 or 4 digits
    pattern = r'\b(0?[1-9]|1[0-2])/(0?[1-9]|[1-2][0-9]|3[0-1])/(\d{2}|\d{4})\b'  # Define your regex pattern

    results = []
    for note in df:
        matches = re.findall(pattern, note)
        # re.findall with groups returns a list of tuples, join them back for the result
        formatted_matches = ['/'.join(match) for match in matches]
        results.extend(formatted_matches)

    return results

